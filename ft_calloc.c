#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	size_t	tot_size;
	void	*dst;

	tot_size = size * count;
	if (!(dst = (void *)malloc(tot_size)))
		return (NULL);
	ft_memset(dst, 0, tot_size);
	return (dst);
}
